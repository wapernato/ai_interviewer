package org.example.dto.response;

public class TopicResponse {

    private Long id;
    private String name;

    public TopicResponse() {
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
